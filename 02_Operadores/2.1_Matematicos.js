// Operadores matemáticos com exemplos do dia a dia

// Adição: somando o valor de duas compras
let valorCompra1 = 35.50;
let valorCompra2 = 24.90;
let totalCompras = valorCompra1 + valorCompra2;
console.log("Total das compras:", totalCompras); // 60.4

// Subtração: calculando o troco ao pagar uma conta
let valorPago = 100;
let valorConta = 73.25;
let troco = valorPago - valorConta;
console.log("Troco:", troco); // 26.75

// Multiplicação: preço de um produto vezes a quantidade comprada
let precoProduto = 12.99;
let quantidade = 4;
let totalProdutos = precoProduto * quantidade;
console.log("Total a pagar pelos produtos:", totalProdutos); // 51.96

// Divisão: dividindo uma pizza entre amigos
let fatiasPizza = 8;
let numeroAmigos = 3;
let fatiasPorAmigo = fatiasPizza / numeroAmigos;
console.log("Cada amigo recebe:", fatiasPorAmigo, "fatias"); // 2.666...

// Módulo: verificando quantos doces sobram após dividir igualmente entre crianças
let totalDoces = 17;
let numeroCriancas = 5;
let docesSobrando = totalDoces % numeroCriancas;
console.log("Doces que sobram:", docesSobrando); // 2

// Exponenciação: calculando juros compostos simples (exemplo)
let capital = 1000;
let taxa = 1.05; // 5% de juros ao ano
let anos = 3;
let montante = capital * (taxa ** anos);
console.log("Montante após 3 anos:", montante.toFixed(2)); // 1157.63
