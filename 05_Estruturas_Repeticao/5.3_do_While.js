//Estrutura de Repetição do...While
// A estrutura do...while executa o bloco de código pelo menos uma vez antes de verificar a condição.

let numero = 0;

do {
    numero ++;
    console.log(`Numero atual: ${numero}`);
}while (numero <= 5);
// Neste exemplo, o bloco de código dentro do `do` será executado pelo menos uma vez, mesmo que a condição seja falsa inicialmente.