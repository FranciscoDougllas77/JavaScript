//Orientação a Objetos em JavaScript
// Orientação a Objetos é um paradigma de programação que utiliza objetos para representar dados e comportamentos.

// Em JavaScript, podemos criar objetos usando a sintaxe de classes introduzida no ES6.

// Classes são uma forma de definir objetos e seus comportamentos de maneira mais estruturada e legível.

// A palavra-chave `this` é usada dentro de uma classe para referenciar a instância atual do objeto.

// Exemplo de classe simples

class Person {
    constructor(name, age){
        this.name = name;
        this.age = age;
    }
}